function bjSuit(card) { return typeof card === "number" ? bjHeart : card.s; }
function bjLabel(card) {
  const v = bjVal(card);
  if (v === 1) return "A";
  if (v === 11) return "J";
  if (v === 12) return "Q";
  if (v === 13) return "K";
  return "" + v;
}
function bjTotal(hand) {
  let total = 0, aces = 0;
  for (const card of hand) {
    const v = bjVal(card);
    if (v === 1) { total += 11; aces++; }
    else total += Math.min(v, 10);
  }
  while (total > 21 && aces > 0) { total -= 10; aces--; }
  return total;
}
